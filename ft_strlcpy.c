#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	l_src;

	l_src = ft_strlen(src);
	i = 0;
	if (dstsize == 0)
	{
		return (l_src);
	}
	if (src != NULL && dst != NULL)
	{
		while (src[i] != '\0' && i < (dstsize - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (l_src);
}
